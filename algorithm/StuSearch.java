package algorithm;

public class StuSearch {
	private String name;
	private String no;
	
	public StuSearch(String name,String no) {
		super();
		this.name=name;
		this.no=no;
	}
	
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getNo() {
		return no;
	}
	public void setNo(String no) {
		this.no = no;
	}
	
	
}
